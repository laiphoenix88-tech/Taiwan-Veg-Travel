const state = {
  language: 'en',
  category: 'all',
  travelType: 'all',
  city: 'all',
  search: '',
};

document.addEventListener('DOMContentLoaded', () => {
  populateLanguageOptions();
  bindFilterEvents();
  setLanguage(state.language);
  document.getElementById('search-input').value = state.search;
});

function getStrings() {
  return translations[state.language] || translations.en;
}

function populateLanguageOptions() {
  const select = document.getElementById('language-select');
  select.innerHTML = '';
  Object.entries(translations).forEach(([code, data]) => {
    const option = document.createElement('option');
    option.value = code;
    option.textContent = data.languageName;
    if (code === state.language) {
      option.selected = true;
    }
    select.appendChild(option);
  });
}

function bindFilterEvents() {
  document.getElementById('language-select').addEventListener('change', (event) => {
    setLanguage(event.target.value);
  });

  document.getElementById('category-select').addEventListener('change', (event) => {
    state.category = event.target.value;
    renderResults();
  });

  document.getElementById('travel-type-select').addEventListener('change', (event) => {
    state.travelType = event.target.value;
    renderResults();
    renderItineraries();
  });

  document.getElementById('city-select').addEventListener('change', (event) => {
    state.city = event.target.value;
    renderResults();
  });

  document.getElementById('search-input').addEventListener('input', (event) => {
    state.search = event.target.value;
    renderResults();
  });
}

function setLanguage(lang) {
  state.language = translations[lang] ? lang : 'en';
  document.getElementById('language-select').value = state.language;
  document.documentElement.lang = state.language;
  document.title = getStrings().appTitle;
  updateStaticText();
  populateFilterOptions();
  renderResults();
  renderItineraries();
}

function updateStaticText() {
  const strings = getStrings();
  document.getElementById('app-title').textContent = strings.appTitle;
  document.getElementById('app-tagline').textContent = strings.appTagline;
  document.getElementById('language-label').textContent = strings.filters.language;
  document.getElementById('category-label').textContent = strings.filters.category;
  document.getElementById('travel-type-label').textContent = strings.filters.travelType;
  document.getElementById('city-label').textContent = strings.filters.city;
  document.getElementById('search-label').textContent = strings.filters.search;
  document.getElementById('results-title').textContent = strings.resultsTitle;
  document.getElementById('itinerary-title').textContent = strings.itineraryTitle;
  document.getElementById('footer-text').textContent = strings.footer;
}

function populateFilterOptions() {
  const strings = getStrings();
  const categorySelect = document.getElementById('category-select');
  const travelSelect = document.getElementById('travel-type-select');
  const citySelect = document.getElementById('city-select');
  const searchInput = document.getElementById('search-input');

  const categoryOptions = [
    { value: 'all', label: strings.categories.all },
    ...Object.entries(strings.categories)
      .filter(([key]) => key !== 'all')
      .map(([key, label]) => ({ value: key, label })),
  ];

  const travelOptions = [
    { value: 'all', label: strings.travelTypes.all },
    ...Object.entries(strings.travelTypes)
      .filter(([key]) => key !== 'all')
      .map(([key, label]) => ({ value: key, label })),
  ];

  const cityOptions = getCityOptions();

  setSelectOptions(categorySelect, categoryOptions, state.category);
  setSelectOptions(travelSelect, travelOptions, state.travelType);
  setSelectOptions(citySelect, cityOptions, state.city);

  searchInput.setAttribute('placeholder', strings.filters.placeholder);
  searchInput.setAttribute('aria-label', strings.filters.search);
  searchInput.value = state.search;
}

function getCityOptions() {
  const strings = getStrings();
  const options = [
    { value: 'all', label: strings.filters.allCities },
  ];
  const map = new Map();
  experiences.forEach((exp) => {
    const key = `${exp.location.city}__${exp.location.area}`;
    if (!map.has(key)) {
      map.set(key, {
        value: key,
        label: `${exp.location.area} · ${exp.location.city}`,
      });
    }
  });
  const sorted = Array.from(map.values()).sort((a, b) =>
    a.label.localeCompare(b.label, state.language, { sensitivity: 'base' })
  );
  return options.concat(sorted);
}

function setSelectOptions(select, options, currentValue) {
  select.innerHTML = '';
  options.forEach((option) => {
    const opt = document.createElement('option');
    opt.value = option.value;
    opt.textContent = option.label;
    if (option.value === currentValue) {
      opt.selected = true;
    }
    select.appendChild(opt);
  });
}

function renderResults() {
  const strings = getStrings();
  const list = document.getElementById('results-list');
  list.innerHTML = '';
  const filtered = filterExperiences();

  if (filtered.length === 0) {
    const empty = document.createElement('li');
    empty.className = 'card';
    const message = document.createElement('div');
    message.className = 'card-content';
    message.textContent = strings.noResults;
    empty.appendChild(message);
    list.appendChild(empty);
    return;
  }

  filtered.forEach((exp) => {
    const card = document.createElement('li');
    card.className = 'card';

    if (exp.media && exp.media.src) {
      const figure = document.createElement('figure');
      figure.className = 'card-media';
      const img = document.createElement('img');
      img.src = exp.media.src;
      const altText =
        getLocalizedText(exp.media ? exp.media.alt : null) || getLocalizedText(exp.names);
      img.alt = altText;
      img.loading = 'lazy';
      figure.appendChild(img);

      const captionParts = [];
      if (exp.location && exp.location.city) {
        captionParts.push(exp.location.city);
      }
      if (exp.location && exp.location.area) {
        captionParts.push(exp.location.area);
      }
      if (captionParts.length) {
        const caption = document.createElement('figcaption');
        caption.textContent = captionParts.join(' • ');
        figure.appendChild(caption);
      }

      card.appendChild(figure);
    }

    const content = document.createElement('div');
    content.className = 'card-content';

    const title = document.createElement('h3');
    title.textContent = getLocalizedText(exp.names);
    content.appendChild(title);

    if (Array.isArray(exp.travelTypes)) {
      const badges = document.createElement('div');
      badges.className = 'badge-group';
      badges.appendChild(
        createBadge(strings.categories[exp.category] || strings.categories.all)
      );
      if (exp.location && exp.location.region) {
        badges.appendChild(createBadge(exp.location.region));
      }
      exp.travelTypes.forEach((type) => {
        badges.appendChild(createBadge(strings.travelTypes[type] || type));
      });
      content.appendChild(badges);
    }

    const description = document.createElement('p');
    description.className = 'description';
    description.textContent = getLocalizedText(exp.descriptions);
    content.appendChild(description);

    const details = document.createElement('div');
    details.className = 'details';

    const locationParts = [];
    if (exp.location && exp.location.area) {
      locationParts.push(exp.location.area);
    }
    if (exp.location && exp.location.city) {
      locationParts.push(exp.location.city);
    }

    addDetail(details, strings.labels.location, locationParts.join(', '));
    addDetail(details, strings.labels.highlights, getLocalizedText(exp.highlights));

    if (Array.isArray(exp.travelTypes) && exp.travelTypes.length) {
      const fits = exp.travelTypes
        .map((type) => strings.travelTypes[type] || type)
        .join(', ');
      addDetail(details, strings.labels.fits, fits);
    }

    if (Array.isArray(exp.tags) && exp.tags.length) {
      addDetail(
        details,
        strings.labels.specialties,
        exp.tags.map(capitalizeTag).join(', ')
      );
    }

    if (exp.contact) {
      addDetail(details, strings.labels.contact, exp.contact);
    }

    if (exp.hours) {
      addDetail(details, strings.labels.hours, exp.hours);
    }

    content.appendChild(details);
    card.appendChild(content);
    list.appendChild(card);
  });
}

function renderItineraries() {
  const container = document.getElementById('itinerary-content');
  container.innerHTML = '';
  const strings = getStrings();
  const keys = Object.keys(itineraries);
  const orderedKeys =
    state.travelType !== 'all' && keys.includes(state.travelType)
      ? [state.travelType, ...keys.filter((key) => key !== state.travelType)]
      : keys;

  orderedKeys.forEach((key) => {
    const plan = itineraries[key];
    const localized = plan[state.language] || plan.en;
    const wrapper = document.createElement('div');
    wrapper.className = 'itinerary';

    if (state.travelType === key) {
      wrapper.dataset.active = 'true';
    }

    const heading = document.createElement('h3');
    heading.textContent = localized.title;
    wrapper.appendChild(heading);

    const intro = document.createElement('p');
    intro.className = 'description';
    intro.textContent = localized.intro;
    wrapper.appendChild(intro);

    const list = document.createElement('ul');
    localized.days.forEach((day) => {
      const item = document.createElement('li');
      const strong = document.createElement('strong');
      strong.textContent = `${day.title}:`;
      item.appendChild(strong);
      item.appendChild(document.createTextNode(` ${day.items[0]}`));
      list.appendChild(item);
    });
    wrapper.appendChild(list);

    if (localized.tip) {
      const tip = document.createElement('p');
      tip.className = 'description';
      const label = document.createElement('strong');
      label.textContent = `${strings.labels.tip || 'Tip'}:`;
      tip.appendChild(label);
      tip.appendChild(document.createTextNode(` ${localized.tip}`));
      wrapper.appendChild(tip);
    }

    container.appendChild(wrapper);
  });
}

function filterExperiences() {
  const query = state.search.trim().toLowerCase();
  return experiences
    .filter((exp) => {
      if (state.category !== 'all' && exp.category !== state.category) {
        return false;
      }
      if (state.travelType !== 'all' && !exp.travelTypes.includes(state.travelType)) {
        return false;
      }
      if (state.city !== 'all') {
        const key = `${exp.location.city}__${exp.location.area}`;
        if (key !== state.city) {
          return false;
        }
      }
      if (query && !matchesSearch(exp, query)) {
        return false;
      }
      return true;
    })
    .sort((a, b) => {
      const cityCompare = (a.location.city || '').localeCompare(
        b.location.city || '',
        state.language,
        { sensitivity: 'base' }
      );
      if (cityCompare !== 0) {
        return cityCompare;
      }
      const nameA = getLocalizedText(a.names);
      const nameB = getLocalizedText(b.names);
      return nameA.localeCompare(nameB, state.language, { sensitivity: 'base' });
    });
}

function matchesSearch(exp, query) {
  const values = [
    ...Object.values(exp.names || {}),
    ...Object.values(exp.descriptions || {}),
    ...Object.values(exp.highlights || {}),
    exp.location ? exp.location.city : undefined,
    exp.location ? exp.location.area : undefined,
    exp.location ? exp.location.region : undefined,
    ...(Array.isArray(exp.tags) ? exp.tags : []),
  ];
  return values.some((value) =>
    value ? value.toLowerCase().includes(query) : false
  );
}

function getLocalizedText(map) {
  if (!map) {
    return '';
  }
  return map[state.language] || map.en || Object.values(map)[0] || '';
}

function createBadge(text) {
  const badge = document.createElement('span');
  badge.className = 'badge';
  badge.textContent = text;
  return badge;
}

function addDetail(container, label, value) {
  if (!value) {
    return;
  }
  const span = document.createElement('span');
  const strong = document.createElement('strong');
  strong.textContent = `${label}:`;
  span.appendChild(strong);
  span.appendChild(document.createTextNode(` ${value}`));
  container.appendChild(span);
}

function capitalizeTag(tag) {
  return tag
    .split(' ')
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ');
}
